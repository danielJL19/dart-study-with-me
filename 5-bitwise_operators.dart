void main() {
  // & esto es para los bits, es decir luego de desmenuzarlo en bits, se compara si son 1 o 0, si es 1 retorna 1, si el par es 0, retorna 0
  int result = 24 & 48;
  print(result);

  // | retorna 1, si hay un "1" en el par
  int result1 = 4 | 3;
  print(result1);

  // XOR retorna si el par es diferente
  int result2 = 10 ^ 8;
  print(result2);
}  